En 1991 en Apartado, bajo el espíritu pujante de un ingeniero mecánico que aporta sólo su capital intelectual y dos equipos de soldadura, establece un pequeño taller de cerrajería en un local en el centro de esta ciudad, bajo el nombre TALLER INGED.
