En el 2009, tomando sugerencias e inquietudes de los clientes, surgen cambios importantes, entre ellos, pasa de persona natural a jurídica, bajo el nombre de INGENIERIA PARA EL DESARROLLO DE LA INDUSTRIA E.U (INGED DE COLOMBIA E.U) empresa unipersonal, para 6 meses después y basados en la ley 1258 de 2008 se transforma en una sociedad por acciones simplificadas, INGENIERIA PARA EL DESARROLLO DE LA INDUSTRIA S.AS. Bajo sus siglas (INGED DE COLOMBIA S.A.S).
Desde entonces, ha impulsado el desarrollo tecnológico, modernización de sus equipos, automatización de sus procesos, ampliación de sus instalaciones e incremento de la planta de personal, logrando con ello, satisfacer en forma óptima la demanda de artefactos marinos y todo lo relacionado con la ingeniería mecánica; convirtiéndose en empresa líder en este campo.
INGED de Colombia S.A.S, es una empresa que desde sus inicios ha presentado un comportamiento progresivo en su nivel de crecimiento y rentabilidad operativa, lo que ha permitido modernización y adquisición de equipos de soldadura, muebles, enceres, y capacitación del personal; lo anterior se ha dado con el fin de cumplir la visión empresarial, la cual establece que para el año 2025 INGED de Colombia S.A.S será la empresa líder en servicios en el campo de la ingeniería mecánica, para la Zona de Urabá.
